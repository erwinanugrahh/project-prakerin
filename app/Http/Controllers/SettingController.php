    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if($request->hasFile('logo')){
            $ext = '.'.$request->file('logo')->getClientOriginalExtension();
            $request->file('logo')->move(public_path(), 'logo'.$ext);
            $request = $request->merge(['logo'=>'logo'.$ext]);
        }else{
            $request = $request->merge(['logo'=>'logo.png']);
        }

        Setting::updateOrCreate([
            'key'=>$request->input('key'),
        ],[
            'value'=>json_encode($request->except('_token','key'))
        ]);
        return back()->with('success','Pengaturan berhasil diedit');
    }

